
DROP TABLE IF EXISTS TBL_ASSINATURA_ENDERECO;
DROP TABLE IF EXISTS TBL_ASSINATURA;
DROP TABLE IF EXISTS TBL_PESSOA;
DROP TABLE IF EXISTS TBL_USER;

CREATE TABLE TBL_USER (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  name VARCHAR(250) NOT NULL,
  pass VARCHAR(250) NOT NULL
);

CREATE TABLE TBL_PESSOA (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  nome VARCHAR(250) NOT NULL
);

CREATE TABLE TBL_ASSINATURA (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  pessoa_id INT NULL,
  produto_id INT NULL
);

CREATE TABLE TBL_ASSINATURA_ENDERECO (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  assinatura_id INT NULL,
  tipo_endereco VARCHAR(250) NOT NULL,
  cep INT(11) NOT NULL
);